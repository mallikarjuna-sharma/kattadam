/**
 * Verify Supabase env vars, enquiries table, and migration 005 contact columns.
 * Usage: npm run verify:supabase
 */
import { readFileSync, existsSync } from "node:fs";
import { createClient } from "@supabase/supabase-js";

function loadEnvLocal() {
  const path = ".env.local";
  if (!existsSync(path)) return;
  for (const line of readFileSync(path, "utf8").split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    if (!process.env[key]) process.env[key] = val;
  }
}

function fail(msg) {
  console.error(`\n✗ ${msg}\n`);
  process.exit(1);
}

function ok(msg) {
  console.log(`✓ ${msg}`);
}

loadEnvLocal();

const url = process.env.NEXT_PUBLIC_SUPABASE_URL?.trim().replace(/\/+$/, "");
const key = process.env.SUPABASE_SERVICE_ROLE_KEY?.trim();

if (!url) fail("NEXT_PUBLIC_SUPABASE_URL is missing. Copy .env.example to .env.local and fill it in.");
if (!key) fail("SUPABASE_SERVICE_ROLE_KEY is missing. Copy .env.example to .env.local and fill it in.");
if (url.includes("YOUR_PROJECT_REF")) fail("Replace YOUR_PROJECT_REF in NEXT_PUBLIC_SUPABASE_URL with your real Supabase project ref.");
if (key.includes("your-service-role-key")) fail("Replace the placeholder SUPABASE_SERVICE_ROLE_KEY with your real service role key.");

ok(`Supabase URL set (${url})`);

const client = createClient(url, key, {
  auth: { persistSession: false, autoRefreshToken: false },
});

const { error: usersError } = await client.from("users").select("id", { head: true });
if (usersError) {
  fail(`Cannot reach Supabase or \`users\` table: ${usersError.message}\n  → Run migrations/001_initial.sql in Supabase SQL Editor.`);
}
ok("`users` table reachable");

const { error: enquiriesError } = await client.from("enquiries").select("id", { head: true });
if (enquiriesError) {
  fail(`\`enquiries\` table missing or inaccessible: ${enquiriesError.message}\n  → Run migrations/001_initial.sql in Supabase SQL Editor.`);
}
ok("`enquiries` table exists");

const { error: contactColsError } = await client
  .from("enquiries")
  .select("phone, alt_phone, email", { head: true });
if (contactColsError) {
  fail(
    `Contact columns missing on \`enquiries\`: ${contactColsError.message}\n  → Run migrations/005_enquiries_contact_fields.sql in Supabase SQL Editor.`
  );
}
ok("Contact columns present (phone, alt_phone, email — migration 005)");

const testPayload = {
  customer_name: "__verify_supabase_test__",
  phone: "9999999999",
  material_label: "verify-script",
  notes: "Auto-generated by npm run verify:supabase — safe to delete",
  status: "pending",
};

const { data: inserted, error: insertError } = await client
  .from("enquiries")
  .insert(testPayload)
  .select("id")
  .single();

if (insertError) {
  fail(`Test insert into \`enquiries\` failed: ${insertError.message}`);
}

ok(`Test enquiry inserted (id: ${inserted.id})`);

const { error: deleteError } = await client.from("enquiries").delete().eq("id", inserted.id);
if (deleteError) {
  console.warn(`⚠ Could not delete test enquiry ${inserted.id}: ${deleteError.message}`);
} else {
  ok("Test enquiry cleaned up");
}

console.log("\nAll checks passed. Enquiry submission should work.\n");
